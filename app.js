// Astronomical calculations for sunrise/sunset
// Based on original MyCode.js implementation

function julianDay(day, month, year, hour) {
    let y = year;
    let m = month;
    
    if (m <= 2) {
        y = y - 1;
        m = m + 12;
    }
    
    const A = Math.floor(y / 100);
    const B = 2 - A + Math.floor(A / 4);
    const jd = Math.floor(365.25 * (y + 4716)) + Math.floor(30.6001 * (m + 1)) + day + B - 1524.5;
    
    return jd + hour / 24.0;
}

function computeGHA(day, month, year, hour) {
    const jd = julianDay(day, month, year, hour);
    const T = (jd - 2451545.0) / 36525.0;
    let theta = 280.46061837 + 360.98564736629 * (jd - 2451545.0);
    theta = theta + 0.000387933 * T * T - T * T * T / 38710000.0;
    
    while (theta > 360.0) theta -= 360.0;
    while (theta < 0.0) theta += 360.0;
    
    return theta;
}

function sunDecRA(mode, jd) {
    const T = (jd - 2451545.0) / 36525.0;
    const T2 = T * T;
    const T3 = T2 * T;
    
    let L = 280.46646 + 36000.76983 * T + 0.0003032 * T2;
    let M = 357.52911 + 35999.05029 * T - 0.0001537 * T2;
    
    while (L > 360.0) L -= 360.0;
    while (L < 0.0) L += 360.0;
    while (M > 360.0) M -= 360.0;
    while (M < 0.0) M += 360.0;
    
    const Mrad = M * Math.PI / 180.0;
    const C = (1.914602 - 0.004817 * T - 0.000014 * T2) * Math.sin(Mrad) +
              (0.019993 - 0.000101 * T) * Math.sin(2 * Mrad) +
              0.000289 * Math.sin(3 * Mrad);
    
    let sunLon = L + C;
    while (sunLon > 360.0) sunLon -= 360.0;
    while (sunLon < 0.0) sunLon += 360.0;
    
    const epsilon = 23.439291 - 0.0130042 * T;
    const epsilonRad = epsilon * Math.PI / 180.0;
    const sunLonRad = sunLon * Math.PI / 180.0;
    
    if (mode === 1) {
        // Declination
        const dec = Math.asin(Math.sin(epsilonRad) * Math.sin(sunLonRad));
        return dec * 180.0 / Math.PI;
    } else {
        // Right Ascension
        let ra = Math.atan2(Math.cos(epsilonRad) * Math.sin(sunLonRad), Math.cos(sunLonRad));
        ra = ra * 180.0 / Math.PI;
        if (ra < 0.0) ra += 360.0;
        return ra;
    }
}

function computeHeight(dec, latitude, longitude, gha) {
    const K = Math.PI / 180.0;
    const latK = latitude * K;
    const decK = dec * K;
    const lha = gha + longitude;
    const lhaK = lha * K;
    
    const sinHeight = Math.sin(decK) * Math.sin(latK) + 
                      Math.cos(decK) * Math.cos(latK) * Math.cos(lhaK);
    
    return Math.asin(sinHeight) / K;
}

function QUAD(yMinus, y0, yPlus) {
    const nz = { count: 0, zero1: 0, zero2: 0, ye: 0, xe: 0 };
    
    const a = 0.5 * (yMinus + yPlus) - y0;
    const b = 0.5 * (yPlus - yMinus);
    const c = y0;
    
    nz.xe = -b / (2.0 * a);
    nz.ye = (a * nz.xe + b) * nz.xe + c;
    
    const dis = b * b - 4.0 * a * c;
    
    if (dis >= 0) {
        const dx = 0.5 * Math.sqrt(dis) / Math.abs(a);
        nz.zero1 = nz.xe - dx;
        nz.zero2 = nz.xe + dx;
        
        if (Math.abs(nz.zero1) <= 1.0) nz.count++;
        if (Math.abs(nz.zero2) <= 1.0) nz.count++;
        if (nz.zero1 < -1.0) nz.zero1 = nz.zero2;
    }
    
    return nz;
}

function calculateSunriseSunset() {
    const day = currentDate.getDate();
    const month = currentDate.getMonth() + 1;
    const year = currentDate.getFullYear();
    
    const twilight = -0.8333; // Civil twilight
    const K = Math.PI / 180.0;
    const sh = Math.sin(K * twilight);
    
    let RISE = false;
    let SETT = false;
    let UTRISE = 0;
    let UTSET = 0;
    let ABOVE = false;
    
    // Calculate proper DST for Greece
    // DST starts last Sunday of March, ends last Sunday of October
    const isDST = (() => {
        // Before March or after October
        if (month < 3 || month > 10) return false;
        if (month > 3 && month < 10) return true;
        
        // Find last Sunday of the month
        const lastDay = new Date(year, month, 0).getDate(); // Last day of month
        let lastSunday = lastDay;
        const lastDayOfWeek = new Date(year, month - 1, lastDay).getDay();
        lastSunday = lastDay - lastDayOfWeek;
        
        if (month === 3) {
            // March: DST starts at 3:00 AM on last Sunday
            return day > lastSunday;
        } else {
            // October: DST ends at 4:00 AM on last Sunday
            return day < lastSunday;
        }
    })();
    
    const offset = isDST ? 3 : 2;
    
    // Scan 24 hours in UTC
    for (let hour = 0; hour < 24; hour++) {
        // Calculate for hour-1, hour, hour+1 (in UTC)
        const jdMinus = julianDay(day, month, year, hour - 1);
        const jd0 = julianDay(day, month, year, hour);
        const jdPlus = julianDay(day, month, year, hour + 1);
        
        const decMinus = sunDecRA(1, jdMinus);
        const dec0 = sunDecRA(1, jd0);
        const decPlus = sunDecRA(1, jdPlus);
        
        // Get Right Ascension of the Sun
        const raMinus = sunDecRA(2, jdMinus);
        const ra0 = sunDecRA(2, jd0);
        const raPlus = sunDecRA(2, jdPlus);
        
        // Calculate GMST
        const gmstMinus = computeGHA(day, month, year, hour - 1);
        const gmst0 = computeGHA(day, month, year, hour);
        const gmstPlus = computeGHA(day, month, year, hour + 1);
        
        // GHA of Sun = GMST - RA
        const ghaMinus = gmstMinus - raMinus;
        const gha0 = gmst0 - ra0;
        const ghaPlus = gmstPlus - raPlus;
        
        const hMinus = computeHeight(decMinus, lat, longit, ghaMinus);
        const h0 = computeHeight(dec0, lat, longit, gha0);
        const hPlus = computeHeight(decPlus, lat, longit, ghaPlus);
        
        const yMinus = Math.sin(K * hMinus) - sh;
        const y0 = Math.sin(K * h0) - sh;
        const yPlus = Math.sin(K * hPlus) - sh;
        
        const quad = QUAD(yMinus, y0, yPlus);
        
        if (y0 > 0.0) ABOVE = true;
        
        if (quad.count === 1) {
            if (yMinus < 0.0) {
                UTRISE = hour + quad.zero1;
                RISE = true;
            } else {
                UTSET = hour + quad.zero1;
                SETT = true;
            }
        } else if (quad.count === 2) {
            if (quad.ye < 0.0) {
                UTRISE = hour + quad.zero2;
                UTSET = hour + quad.zero1;
            } else {
                UTRISE = hour + quad.zero1;
                UTSET = hour + quad.zero2;
            }
            RISE = true;
            SETT = true;
        }
        
        if (RISE && SETT) break;
    }
    
    // Format output
    let riseStr = '--:--';
    let setStr = '--:--';
    
    if (RISE) {
        let riseLocal = UTRISE + offset;
        if (riseLocal >= 24) riseLocal -= 24;
        if (riseLocal < 0) riseLocal += 24;
        riseStr = formatTime(riseLocal);
    } else if (ABOVE) {
        riseStr = 'visible';
    }
    
    if (SETT) {
        let setLocal = UTSET + offset;
        if (setLocal >= 24) setLocal -= 24;
        if (setLocal < 0) setLocal += 24;
        setStr = formatTime(setLocal);
    } else if (ABOVE) {
        setStr = 'visible';
    }
    
    if (language === 0) {
        return `Α. ${riseStr}, Δ. ${setStr}`;
    } else {
        return `Sun: ${riseStr}, ${setStr}`;
    }
}

function formatTime(hours) {
    const h = Math.floor(hours);
    const m = Math.round((hours - h) * 60);
    if (m === 60) {
        return `${(h + 1).toString().padStart(2, '0')}:00`;
    }
    return `${h.toString().padStart(2, '0')}:${m.toString().padStart(2, '0')}`;
}

// Moon phase calculations
function getPhaseOfTheMoon(date) {
    const day = date.getDate();
    const month = date.getMonth() + 1;
    const year = date.getFullYear();
    const J = julianDay(day, month, year, 12);
    let M = (J + 4.867) / 29.53058;
    M = 2 * (M - Math.floor(M)) - 1;
    return Math.abs(M);
}

function getMoonAge() {
    const searchDate = new Date(currentDate);
    let minPhase = 10;
    let maxPhase = -10;
    let newMoon = null;
    let fullMoon = null;
    
    // Search 30 days around current date
    searchDate.setDate(searchDate.getDate() - 15);
    
    for (let i = 0; i < 30; i++) {
        const phase = getPhaseOfTheMoon(searchDate);
        
        if (phase < minPhase) {
            minPhase = phase;
            newMoon = new Date(searchDate);
        }
        
        if (phase > maxPhase) {
            maxPhase = phase;
            fullMoon = new Date(searchDate);
        }
        
        searchDate.setDate(searchDate.getDate() + 1);
    }
    
    // Check if today is full or new moon
    if (fullMoon && isSameDate(fullMoon, currentDate)) {
        return language === 0 ? "Πανσέληνος" : "Full moon";
    }
    
    if (newMoon && isSameDate(newMoon, currentDate)) {
        return language === 0 ? "Νέα Σελήνη" : "New moon";
    }
    
    // Calculate moon age
    const daysFromFull = daysBetweenDates(currentDate, fullMoon);
    let age;
    
    if (daysFromFull <= 0) {
        age = 15 - (daysFromFull + 1);
    } else {
        age = 15 - daysFromFull - 2;
    }
    
    if (age < 0) age += 30;
    if (age > 30) age -= 30;
    
    if (language === 0) {
        return `Σελήνη ${Math.abs(age)} ημερών`;
    } else {
        return `${Math.abs(age)} days moon`;
    }
}

function isSameDate(date1, date2) {
    return date1.getDate() === date2.getDate() &&
           date1.getMonth() === date2.getMonth() &&
           date1.getFullYear() === date2.getFullYear();
}

