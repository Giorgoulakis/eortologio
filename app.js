// Date manipulation functions
function incDateByDays(dt, days) {
    const newDate = new Date(dt);
    newDate.setDate(newDate.getDate() + days);
    return newDate;
}

function daysBetweenDates(date1, date2) {
    const oneDay = 24 * 60 * 60 * 1000;
    return Math.round((date2.getTime() - date1.getTime()) / oneDay);
}

// Calculate Pascha (Easter) date using Gaussian algorithm
function getPasxaDate(year) {
    const a = year % 19;
    const b = year % 4;
    const g = year % 7;
    const d = (19 * a + 16) % 30;
    const j = (2 * b + 4 * g + 6 * d) % 7;
    const p = d + j + 3;
    
    const pasxaDate = new Date(year, 3, 1); // April 1st
    return incDateByDays(pasxaDate, p - 1);
}

